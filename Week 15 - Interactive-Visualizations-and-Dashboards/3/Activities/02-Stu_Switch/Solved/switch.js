// Switch function
function chooseLetter(letter) {
    switch(letter) {
        case "A":
          console.log(1);
          break;
        case "B":
          console.log(2);
          break;
        case "C":
          console.log(3);
          break;
        case "D":
          console.log(4);
          break;
        case "E":
          console.log(5);
          break;
        default:
          console.log(0);
          break;
    }
}

chooseLetter("F");
